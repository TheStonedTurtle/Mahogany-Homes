package thestonedturtle.mahoganyhomes;

public class RequiredMaterials {

	public Integer MinPlanks;
	public Integer MaxPlanks;
	public Integer MinSteelBars;
	public Integer MaxSteelBars;

	public RequiredMaterials(int minPlanks, int maxPlanks, int minSteelBars, int maxSteelBars) {
		MinPlanks = minPlanks;
		MaxPlanks = maxPlanks;
		MinSteelBars = minSteelBars;
		MaxSteelBars = maxSteelBars;
	}
}
